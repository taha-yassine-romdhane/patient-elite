// Helper function to get delegations for a specific region
export const getDelegationsForRegion = (regionName: string): string[] => {
  const region = tunisianRegions.find(r => r.name === regionName);
  return region ? region.delegations : [];
};

// Get all region names
export const getAllRegionNames = (): string[] => {
  return tunisianRegions.map(region => region.name);
};
